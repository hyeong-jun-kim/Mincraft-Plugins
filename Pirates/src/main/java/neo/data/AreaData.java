package neo.data;

public class AreaData {
    public int x1;
    public int x2;
    public int y;
    public int z1;
    public int z2;
    public AreaData(int x1, int x2, int y, int z1, int z2) {
        this.x1 = x1;
        this.x2 = x2;
        this.y = y;
        this.z1 = z1;
        this.z2 = z2;
    }
}
